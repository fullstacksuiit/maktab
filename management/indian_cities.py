def get_city_choices(state=None):
    """Return city choices for a given state, or all cities."""
    if state and state in CITY_DATA:
        choices = [('', 'Select City')]
        for city in sorted(CITY_DATA[state].keys()):
            choices.append((city, city))
        return choices
    return [('', 'Select City')]


def get_coordinates(state, city):
    """Return (latitude, longitude) for a given state and city, or None."""
    if state in CITY_DATA and city in CITY_DATA[state]:
        return CITY_DATA[state][city]
    return None
